package classemath;

//@author Julianoacs

public class ClasseMath {

    public static void main(String[] args) {
        double pi = Math.PI;//numero de pi
        double pow = Math.pow(5, 2);//Exponeciacao
        double sqrt = Math.sqrt(25);//Raiz Quadrada
        double cbrt = Math.cbrt(27);//Raiz Cubica
        double ran = Math.random();//Numero Random
        double abs = Math.abs(-10);//Valor Absoluto
        double floor = Math.floor(3.9);//Arredonda para Baixo
        double ceil = Math.ceil(4.2);//Arredonda para cima
        double round = Math.round(5.6);//Arredondamento Aritmetico
        System.out.println(pi);
        System.out.println(pow);
        System.out.println(sqrt);
        System.out.println(cbrt);
        System.out.println(ran);
        System.out.println(abs);
        System.out.println(floor);
        System.out.println(ceil);
        System.out.println(round); 
    }
    
}
